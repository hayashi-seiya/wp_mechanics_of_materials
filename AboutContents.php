<h2 class="h2">材料力学を学ぶとは？</h2>

<h3 class="h3" id="01">材料力学とは</h3>
<div class="introContents">
  <p>
    機械や構造物に作用する種々の外力に対し、各部材の
    <span class="strong">強さ(strength)</span>、
    <span class="strong">剛性(stiffness)</span>
    および
    <span class="strong">安定性(stability)</span>
    がどのようになっているか、
    理論と実験の両面から明らかにしておく必要がある。
    このような学問を<span class="strong">材料力学 (mechanics of materials)</span>という。
  </P>
  <p>
    機械や構造物を構成している部材は一見すると複雑な形状をしているようであるが、
    これらは単純な構造要素に分解できるものが多く、通常は棒、平板あるいは曲面版などに分類できる。
    このような機械や構造物が外力を受けると、各部材は伸び、縮み、ねじり、曲げなどの変形を生じる。
    変形が過大になり、部材内部に生じる抵抗力がある限度を越えると、部材は使用に耐えられなくなり、
    ついには破壊に至る。
  </p>
  <p>
    したがって、機械や構造物を設計する場合には、部材が破壊しないように十分な
    <span class="strong">強さ</span>を持ち、過大な変形を起こさないように軽量で適切な
    <span class="strong">剛性</span>を備え、常に形態の
    <span class="strong">安定性</span>を保持できるように、
    部材の寸法や形状を経済的・合理的・機能的に決定することが大切であり、その土台となる学問が
    <span class="strong">材料力学</span>である。
  </p>

